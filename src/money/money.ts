export type CurrencyCode = keyof typeof SUPPORTED_CURRENCIES;
export type Currency = (typeof SUPPORTED_CURRENCIES)[CurrencyCode];

export type MinorToMajorTransform<TOutput> = (props: { value: string; currency: Currency }) => TOutput;

export function resolveCurrency(currencyCode: CurrencyCode): Currency {
  return SUPPORTED_CURRENCIES[currencyCode];
}

export function minorToMajor<TOutput = string>(
  minor: bigint,
  currencyCode: CurrencyCode,
  transform?: MinorToMajorTransform<TOutput>,
): string | TOutput {
  const currency = resolveCurrency(currencyCode);
  const amount = dinero({ amount: minor, currency });

  if (!transform) {
    return toDecimal(amount);
  }

  return toDecimal(amount, ({ value, currency: resolvedCurrency }) => transform({ value, currency: resolvedCurrency }));
}

// Converts a major-unit string to minor-unit bigint, throwing ValidationException (422) on invalid input
export function majorToMinor(major: string, currencyCode: CurrencyCode, field = 'amount'): bigint {
  const { exponent } = resolveCurrency(currencyCode);
  const scale = typeof exponent === 'bigint' ? Number(exponent) : exponent;
  const trimmed = major.trim();

  if (!/^-?\d+(\.\d+)?$/.test(trimmed)) {
    throw new ValidationException({
      detail: `Invalid amount: "${major}".`,
      errors: [{ field, message: 'Enter a valid number.' }],
    });
  }

  const isNegative = trimmed.startsWith('-');
  const unsignedValue = isNegative ? trimmed.slice(1) : trimmed;
  const [wholePart, fractionalPart = ''] = unsignedValue.split('.');

  if (fractionalPart.length > scale) {
    throw new ValidationException({
      detail: `Too many decimal places for ${currencyCode}. Maximum allowed is ${scale}.`,
      errors: [{ field, message: `${currencyCode} allows up to ${scale} decimal place${scale === 1 ? '' : 's'}.` }],
    });
  }

  const paddedFraction = fractionalPart.padEnd(scale, '0');
  const normalizedDigits = `${wholePart}${paddedFraction}`.replace(/^0+(?=\d)/, '');
  const minor = BigInt(normalizedDigits || '0');

  return isNegative ? -minor : minor;
}
